package com.hysoft.model;

public class Pedido {

	private Integer numPeriodo;
	private Integer idContrato;
	private String  indTipoPedido;
	private Integer primeiroAno;
	private Integer segundoAno;
	private Integer terceiroAno;
	private Integer quartoAno;
	private Integer quintoAno;
	
	public Integer getNumPeriodo() {
		return numPeriodo;
	}
	public void setNumPeriodo(Integer numPeriodo) {
		this.numPeriodo = numPeriodo;
	}
	public Integer getIdContrato() {
		return idContrato;
	}
	public void setIdContrato(Integer idContrato) {
		this.idContrato = idContrato;
	}
	public String getIndTipoPedido() {
		return indTipoPedido;
	}
	public void setIndTipoPedido(String indTipoPedido) {
		this.indTipoPedido = indTipoPedido;
	}
	public Integer getPrimeiroAno() {
		return primeiroAno;
	}
	public void setPrimeiroAno(Integer primeiroAno) {
		this.primeiroAno = primeiroAno;
	}
	public Integer getSegundoAno() {
		return segundoAno;
	}
	public void setSegundoAno(Integer segundoAno) {
		this.segundoAno = segundoAno;
	}
	public Integer getTerceiroAno() {
		return terceiroAno;
	}
	public void setTerceiroAno(Integer terceiroAno) {
		this.terceiroAno = terceiroAno;
	}
	public Integer getQuartoAno() {
		return quartoAno;
	}
	public void setQuartoAno(Integer quartoAno) {
		this.quartoAno = quartoAno;
	}
	public Integer getQuintoAno() {
		return quintoAno;
	}
	public void setQuintoAno(Integer quintoAno) {
		this.quintoAno = quintoAno;
	}

}
